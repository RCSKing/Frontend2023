let a = 10;
let b = 5;

function somar(){
    let som = a + b;
    document.getElementById("soma").innerHTML = som;
}

function subtrair(){
    let sub = a - b;
    document.getElementById("subtração").innerHTML = sub;
}

function dividir(){
    let div = a / b;
    document.getElementById("divisão").innerHTML = div;
}

function multiplicar(){
    let mult = a * b;
    document.getElementById("multiplicação").innerHTML = mult;
}

